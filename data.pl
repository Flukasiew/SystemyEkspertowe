% znajduje najczęściej występujący elemnet spośrud przekazanych jako OP w zmiennej globlanej 'ROZ2'
findMax(OP,R):-
	forEach(OP,R,_).
forEach([X|T],WNAME,WIL):-
	forEach(T,NAME2,IL2),
	countRoz2(X,RIL),
	bigger(NAME2,IL2,X,RIL, WNAME,WIL).
forEach([X],X,RIL):-
	countRoz2(X,RIL).
	
bigger(XN,XI,YN,YI,XN,XI):-
	XI >= YI.
bigger(XN,XI,YN,YI,YN,YI):-
	YI > XI.
	
