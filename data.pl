memberOf(X, [X|_]).
memberOf(X, [_|Ys]) :-
    memberOf(X, Ys).

% usuwa wszystkie listy z listy list(2 parametr) które zwierają element przekazany jako 1 parametr 
deletesP(A,[B],[B]):-
	not(memberOf(A,B)).
deletesP(A,[B],[]):-
	memberOf(A,B).
deletesP(_,[],[]).
deletesP(A, [W|B], R):-
	memberOf(A,W),
	deletesP(A,B,R).
deletesP(A, [B, C|D], [B|E]) :-
	not(memberOf(A,B)),
	deletesP(A, [C|D], E).

