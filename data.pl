%zlicza występwania X w znienej globalnej 'ROZ2'
countRoz2(X,RES):-
	R = 0,
	nb_getval('ROZ2',ROZ),
	itCount1(ROZ,X,R,RES).
itCount1([H|T],X,R,RES):-
	count(X,H,R,R1),
	itCount1(T,X,R1,RES).
	
itCount1([],_,R1,RES):-
	RES is R1.

count(_, [], I, R) :-
	R is I.
count(X, [X | T], I,R) :-
  !, count(X, T, I,R1),
  R is R1 + 1.
count(X, [_ | T], I,R) :-
  count(X, T, I,R).
  
  
